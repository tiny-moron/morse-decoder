const MORSE_TABLE = {
    '*': ' ',
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
 var n = 0,
        key = [''],
        i = 0;
    while(n < expr. length) {
        if (expr[n] === '1') {
            if (expr[n + 1] === '0') key[i] += '.';
            else key[i] += '-';
        } else if (expr[n] === '*') {
            key[i] += '*';
            n += 8;
        } 
        if ((n + 2) % 10 == 0) {
            key[i] = MORSE_TABLE[key[i]];
            key.push('');
            i++;
        }
        n += 2;
    }
    return key.join('');
}

module.exports = {
    decode
}
